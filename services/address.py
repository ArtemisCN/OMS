# 中文数字 → 阿拉伯数字映射
_CN_NUM_MAP = {
    '一': '1', '二': '2', '三': '3', '四': '4', '五': '5',
    '六': '6', '七': '7', '八': '8', '九': '9', '十': '10',
    '十一': '11', '十二': '12', '十三': '13', '十四': '14', '十五': '15',
    '十六': '16', '十七': '17', '十八': '18', '十九': '19', '二十': '20',
}

def _normalize_building_name(name):
    """将楼区名中的中文数字转为阿拉伯数字（如：三号楼→3号楼）
    优先匹配长中文数字，避免 \"十三\" 被拆成 \"十\"+\"三\"
    """
    # 按长度降序排列，优先匹配多字符中文数字
    for cn in sorted(_CN_NUM_MAP.keys(), key=lambda x: -len(x)):
        if cn in name:
            return name.replace(cn, _CN_NUM_MAP[cn])
    return name

# 预计算楼区名列表，按名称长度降序排列（长名优先匹配）
_BUILDING_NAMES = sorted(set(a['楼区'] for a in ADDRESS_LIST), key=lambda x: (-len(x), x))
# 预计算科室名列表，按名称长度降序排列（长名优先匹配）
_DEPT_NAMES = sorted(set(a['所属科室'] for a in ADDRESS_LIST), key=lambda x: (-len(x), x))
# 楼层正则表达式：匹配 "数字+楼/F/层"
_FLOOR_PATTERN = re.compile(r'(\d+)[楼F层]')


def extract_address_from_title(title, hospital_id=None):
    """从工单名称中自动提取楼区、楼层、科室、物理地址

    例：'8号楼3楼肾病科电脑无法开机'
    →  {'building': '8号楼', 'floor': '3F', 'department': '肾病科', 'location': ''}

    如果标题中包含已知物理地址（如 '信息中心' '门诊检验8号窗口'），
    则直接返回该地址对应的完整楼区/楼层/科室/位置信息。

    返回 dict，找不到的字段为空字符串

    参数:
        hospital_id: 医院ID，用于过滤地址数据。为None时自动从 g.hospital_id 获取。
    """
    # 标题为空时直接返回空结果
    if not title:
        return {'building': '', 'floor': '', 'department': '', 'location': ''}

    # 初始化结果字典，四个字段均为空字符串
    result = {'building': '', 'floor': '', 'department': '', 'location': ''}

    # 从数据库获取合并后的地址列表（含用户新增的地址），按医院隔离
    merged = get_merged_addresses(hospital_id=hospital_id)
    # 动态获取楼区名和科室名（可能包含数据库新增的条目）
    buildings = sorted(set(a['楼区'] for a in merged), key=lambda x: (-len(x), x))
    depts = sorted(set(a['所属科室'] for a in merged), key=lambda x: (-len(x), x))

    # 1. 提取楼区：在标题中查找已知楼区名（先匹配长名）
    #    同时支持中英文数字格式互转（如：三号楼 ↔ 3号楼）
    for bname in buildings:
        # 如果楼区名出现在标题中，立即匹配
        if bname in title:
            result['building'] = bname
            break
        # 如果楼区名包含中文数字，转为阿拉伯数字后再匹配一次
        alt_name = _normalize_building_name(bname)
        if alt_name != bname and alt_name in title:
            result['building'] = bname
            break

    # 2. 提取楼层：用正则匹配数字，再标准化为 "数字F" 格式
    match = _FLOOR_PATTERN.search(title)
    if match:
        num = match.group(1)
        # 尝试匹配数据库中已有的楼层格式（如 "3F" 或 "3楼"）
        candidates = [f'{num}F', f'{num}楼']
        for c in candidates:
            if any(a['所属楼层'] == c for a in merged):
                result['floor'] = c
                break
        # 如果数据库中无匹配，默认用 "数字F" 格式
        if not result['floor']:
            result['floor'] = f'{num}F'

    # 3. 提取科室：在标题中查找已知科室名（按长度降序，优先匹配最具体的科室）
    for dname in depts:
        # 按长度降序匹配，优先匹配最具体的科室名
        if dname in title:
            result['department'] = dname
            break

    # 4. 尝试从标题中匹配物理地址
    # 先收集需要跳过的物理地址——那些是楼区名子串的短地址（如楼区名含地址名）
    skip_locations = set()
    # 遍历所有楼区名和物理地址，过滤掉冗余短地址
    for bname in buildings:
        for loc in set(a['物理地址'] for a in merged):
            # 如果地址名是楼区名的子串，说明是冗余的（如楼区名"超声医学科"含地址名"医学科"）
            if loc and len(loc) < len(bname) and loc in bname:
                skip_locations.add(loc)

    # 对所有物理地址按长度降序排列，优先匹配最具体的位置名
    all_locations = sorted(
        set(a['物理地址'] for a in merged if a['物理地址'] not in skip_locations),
        key=lambda x: (-len(x), x)
    )
    # 遍历并按最长的位置名优先匹配
    for loc in all_locations:
        # 如果该位置名出现在标题中，则命中
        if loc in title:
            # 找出数据库中所有匹配该物理地址的记录
            loc_records = [a for a in merged if a['物理地址'] == loc]
            # 在匹配记录中，优先选择与已识别科室/楼区一致的那条
            best = None
            # 第一步：尝试匹配已识别的科室
            dept_matches = []
            for a in loc_records:
                # 筛选出与已提取科室同名的记录
                if result['department'] and a['所属科室'] == result['department']:
                    dept_matches.append(a)
            if dept_matches:
                # 如果同科室有多条记录，优先选数据库新增的记录（base_index=-1）
                for a in dept_matches:
                    if a.get('_base_index', 0) < 0:
                        best = a
                        break
                if not best:
                    best = dept_matches[0]
            # 如果没有匹配科室，则尝试按已识别楼区匹配
            if not best and result['building']:
                for a in loc_records:
                    if a['楼区'] == result['building']:
                        best = a
                        break
            # 如果仍无匹配，直接取第一条记录（容错处理）
            if not best:
                best = loc_records[0] if loc_records else None
            # 用匹配到的物理地址记录补全结果中的空缺字段
            if best:
                loc_building = best['楼区']
                loc_floor = best['所属楼层']
                loc_dept = best['所属科室']
                # 如果楼区尚未识别，从物理地址记录中补全
                if not result['building']:
                    result['building'] = loc_building
                # 如果楼层尚未识别，从物理地址记录中补全
                if not result['floor']:
                    result['floor'] = loc_floor
                # 如果科室尚未识别，从物理地址记录中补全
                if not result['department']:
                    result['department'] = loc_dept
                result['location'] = loc
            # 找到一个匹配即退出循环（最长匹配优先）
            break
    # 返回最终提取结果，未匹配的字段为空字符串
    return result
