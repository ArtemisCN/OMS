def parse_addresses_from_text(text):
    """解析原始地址表格文本为结构化列表"""
    # 按换行符切分多行文本
    lines = text.strip().split('\n')
    addresses = []
    # 标记是否已进入表格数据区
    in_table = False
    for line in lines:
        # 跳过元数据行（表格名称、序号等）
        if line.startswith('> metadata.sheet_name:') or line.startswith('> metadata.sheet_index_num:'):
            continue
        # 检查是否为有效的 Markdown 表格行（含 | 分隔符且非表头分隔线）
        if '|' in line and not re.match(r'\|[\s:\-]+\|', line):
            # 按 | 切割并去除首尾空白
            parts = [p.strip() for p in line.split('|')]
            # 如果切割后字段数 >=5，说明首尾有空单元格，去掉首尾
            if len(parts) >= 5:
                parts = parts[1:-1]
            else:
                continue
            # 跳过表头行（第一个字段为"楼区"）
            if parts and parts[0] == '楼区':
                in_table = True
                continue
            # 拆分四列数据：楼区、楼层、科室、物理地址
            if in_table and len(parts) == 4:
                building, floor, dept, physical = parts
                # 跳过空行（四列均为空）
                if building and floor and dept and physical:
                    # 组装成结构化字典并加入列表
                    addresses.append({
                        '楼区': building,
                        '所属楼层': floor,
                        '所属科室': dept,
                        '物理地址': physical
                    })
    return addresses


