def get_addresses_grouped(building=None, keyword=None):
    """获取按楼区分组的地址数据"""
    # 获取合并后的完整地址列表
    merged = get_merged_addresses()
    # 如果指定了楼区，过滤出该楼区的记录
    if building:
        merged = [a for a in merged if a['楼区'] == building]
    # 如果指定了关键词，在楼区/科室/物理地址中模糊匹配
    if keyword:
        merged = [a for a in merged if
                  keyword in a['楼区'] or keyword in a['所属科室'] or keyword in a['物理地址']]
    # 按楼区进行分组
    groups = {}
    # 遍历合并后的地址，按楼区名归类
    for a in merged:
        b = a['楼区']
        # 如果该楼区尚未在分组字典中，初始化空列表
        if b not in groups:
            groups[b] = []
        groups[b].append(a)
    # 按楼区名的数字部分排序（纯中文名排最后）
    from collections import OrderedDict
    # 提取数字作为排序键，无数字的楼区名排末尾（9999）
    # 支持中文数字楼区名（如 一号楼 → 1号楼）
    def _sort_key(x):
        # 先尝试提取阿拉伯数字
        digits = ''.join(c for c in x if c.isdigit())
        if digits:
            return (int(digits or '9999'), x)
        # 没阿拉伯数字时，尝试提取中文数字
        alt = _normalize_building_name(x)
        alt_digits = ''.join(c for c in alt if c.isdigit())
        if alt_digits:
            return (int(alt_digits or '9999'), x)
        return (9999, x)

    sorted_keys = sorted(groups.keys(), key=_sort_key)
    # 构建有序字典，保持排序后的分组顺序
    result = OrderedDict()
    for k in sorted_keys:
        result[k] = groups[k]
    # 返回分组排序后的地址字典
    return result


