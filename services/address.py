def get_merged_addresses(hospital_id=None):
    """获取当前医院的全部地址（RAW数据 + 数据库覆盖，按医院隔离）"""
    from models import AddressOverride, db
    from flask import g

    if hospital_id is None:
        try:
            hospital_id = getattr(g, 'hospital_id', None)
        except RuntimeError:
            hospital_id = None

    # 1. 以 RAW_ADDRESS_TABLE 为基础
    # 使用 dict 以 base_index 为键，方便替换和删除
    base_map = {}  # base_index → entry
    for i, addr in enumerate(ADDRESS_LIST):
        entry = dict(addr)
        entry['_override_id'] = None
        entry['_base_index'] = i
        base_map[i] = entry

    # 2. 加载数据库覆盖记录
    query = AddressOverride.query
    if hospital_id:
        query = query.filter(AddressOverride.hospital_id == hospital_id)
    else:
        # 全量模式（admin 看全部医院）：只加载新增条目（base_index=-1），不替换基础条目
        query = query.filter(AddressOverride.base_index == -1)
    overrides = query.order_by(AddressOverride.id).all()

    deleted_indices = set()
    extra_entries = []  # 新增条目

    for o in overrides:
        if o.is_deleted:
            if hospital_id and o.base_index >= 0:
                deleted_indices.add(o.base_index)
            continue
        entry = {
            '楼区': o.building,
            '所属楼层': o.floor,
            '所属科室': o.department,
            '物理地址': o.location,
            '_override_id': o.id,
            '_base_index': o.base_index if o.base_index >= 0 else -1,
        }
        if o.base_index >= 0:
            base_map[o.base_index] = entry  # 替换基础条目
        else:
            extra_entries.append(entry)  # 新增条目

    # 3. 合并：基础条目（排除已删除的）+ 新增条目
    merged = []
    for i in sorted(base_map.keys()):
        if i not in deleted_indices:
            merged.append(base_map[i])
    merged.extend(extra_entries)

    return merged


