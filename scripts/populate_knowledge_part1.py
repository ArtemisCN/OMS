conn = sqlite3.connect(DB)
cur = conn.cursor()

# Ensure table exists (use same schema as model)
cur.execute("""
CREATE TABLE IF NOT EXISTS knowledge_base (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    title VARCHAR(200) NOT NULL,
    category VARCHAR(50) DEFAULT '公告',
    content TEXT DEFAULT '',
    is_pinned BOOLEAN DEFAULT 0,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
)
""")

existing = set(r[0] for r in cur.execute("SELECT title FROM knowledge_base").fetchall())

added = 0
for title, cat, content in ARTICLES:
    if title in existing:
        continue
    now = datetime.now().strftime('%Y-%m-%d %H:%M:%S')
    cur.execute(
        "INSERT INTO knowledge_base (title, category, content, is_pinned, created_at, updated_at) VALUES (?, ?, ?, 1, ?, ?)",
        (title, cat, content, now, now)
    )
    added += 1

conn.commit()
total = cur.execute("SELECT count(*) FROM knowledge_base").fetchone()[0]
print(f'公告: 新增 {added} 条 (跳过已存在), 总计 {total} 条')
cur.execute("SELECT category, count(*) FROM knowledge_base GROUP BY category")
for r in cur.fetchall():
    print(f'  {r[0]}: {r[1]} 篇')
conn.close()
